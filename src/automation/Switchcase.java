package automation;

public class Switchcase {

	public static void main(String[] args) {
	

		

		char mychar = 'a';
		

		switch(mychar){
			

		case 'a' :
		case 'e' : 
		case 'i' : 
		case 'o' :
		case 'u' : 
				

			System.out.println("This is a vowel");
			break;
			default: 
				System.out.println("this is a consonent");
		}
		

	}

}
