package encapsulation;

public class EncapDemo {
	private String name;
	private int id;
	private int age;
	
	
	public static void main(String[] args) {
		

	}


	public String getName() {
		return name;
	}


	public void setName(String myname) {
		name = myname;
	}


	public int getId() {
		return id;
	}


	public void setId(int myid) {
		id = myid;
	}


	public int getAge() {
		return age;
	}


	public void setAge(int myage) {
		age = myage;
	}

}
